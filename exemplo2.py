# imprimir numeros de ponto floutuantes usando %n1.n2f
 #o primeiro n1 e a quantidade minima de numeros antes da virgula e sera acrescido por espacos antes
print('esse numero é real: %.2f' %(13.144))
# o retorno sera 13.14
print('esse numero é real: %.3f' %(13.144))
# o retorno sera 13.144
print('esse numero é real: %.4f' %(13.144))
# o retorno sera 13.1440( ele adiciona um zero se nao houver outro numero

